#include "wglbx.h"

int	sdigit(char *str)
{
	int	i;

	i = 0;
	if (((str[i] >= 0 && str[i] <= 9)
			|| (str[i] == '+' || str[i] == '-'))
		&& (str[i + 1] != '+' || str[i + 1] != '-'))
		i++;
	else
		return (1);
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' || str[i] <= '9'))
			return (1);
		i++;
	}
	return (0);
}
